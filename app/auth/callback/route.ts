import { createRouteHandlerClient } from '@supabase/auth-helpers-nextjs';
import { cookies } from 'next/headers';
import { NextResponse } from 'next/server';

// Force dynamic rendering
export const dynamic = 'force-dynamic';

/**
 * üîí CALLBACK OAUTH S√âCURIS√â
 * 
 * Ce callback ne fait QUE :
 * 1. √âchanger le code OAuth contre une session
 * 2. V√©rifier que l'utilisateur existe dans public.users (cr√©√© par trigger)
 * 3. Rediriger vers la bonne page
 * 
 * ‚ö†Ô∏è S√âCURIT√â :
 * - Le trigger SQL cr√©e automatiquement l'utilisateur avec le bon r√¥le
 * - Pas de cr√©ation manuelle d'utilisateur (faille de s√©curit√©)
 * - Pas de logs sensibles (tokens, etc.)
 */
export async function GET(request: Request) {
  const requestUrl = new URL(request.url);
  const code = requestUrl.searchParams.get('code');
  const error_description = requestUrl.searchParams.get('error_description');
  
  // V√©rifier aussi les tokens dans le hash (mode Implicit Flow - fallback)
  const hash = requestUrl.hash;
  const hasAccessToken = hash && hash.includes('access_token=');

  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  // 1Ô∏è‚É£ VALIDATION : Erreurs OAuth
  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  if (error_description) {
    console.error('‚ùå OAuth Error:', error_description);
    return NextResponse.redirect(
      new URL(`/auth/login?error=${encodeURIComponent('Erreur d\'authentification')}`, requestUrl.origin)
    );
  }

  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  // 2Ô∏è‚É£ VALIDATION : Code OAuth ou Token pr√©sent
  // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
  if (!code && !hasAccessToken) {
    console.error('‚ùå No OAuth code or token');
    console.log('URL:', requestUrl.href);
    return NextResponse.redirect(
      new URL('/auth/login?error=Code+manquant', requestUrl.origin)
    );
  }
  
  // ‚ö†Ô∏è Si on re√ßoit un token dans le hash au lieu d'un code, 
  // c'est que Google OAuth est mal configur√© (Implicit Flow au lieu de Code Flow)
  if (!code && hasAccessToken) {
    console.warn('‚ö†Ô∏è Received access_token in hash instead of code - OAuth misconfigured!');
    console.warn('üëâ Fix: Supabase Dashboard ‚Üí Auth ‚Üí Providers ‚Üí Google ‚Üí Check redirect URL');
    console.warn('üëâ Should be: https://[PROJECT].supabase.co/auth/v1/callback');
    
    // On redirige quand m√™me vers le dashboard avec le token
    // Le navigateur va g√©rer la session via le hash fragment
    return NextResponse.redirect(new URL('/dashboard', requestUrl.origin));
  }

  try {
    const supabase = createRouteHandlerClient({ cookies });
    
    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    // 3Ô∏è‚É£ √âCHANGE : Code OAuth ‚Üí Session
    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    const { data: sessionData, error: sessionError } = await supabase.auth.exchangeCodeForSession(code);
    
    if (sessionError || !sessionData?.session || !sessionData?.user) {
      console.error('‚ùå Session error:', sessionError?.message);
      return NextResponse.redirect(
        new URL('/auth/login?error=Session+invalide', requestUrl.origin)
      );
    }

    const user = sessionData.user;
    
    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    // 4Ô∏è‚É£ ATTENTE : Trigger cr√©e l'utilisateur (asynchrone)
    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    // Le trigger SQL prend ~500ms pour s'ex√©cuter
    await new Promise(resolve => setTimeout(resolve, 1000));
    
    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    // 5Ô∏è‚É£ V√âRIFICATION : Utilisateur existe dans public.users
    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    const { data: profile, error: profileError } = await supabase
      .from('users')
      .select('id, email, role, status')
      .eq('id', user.id)
      .single();
    
    if (profileError || !profile) {
      // Si profil non trouv√©, r√©essayer une fois (trigger peut √™tre lent)
      await new Promise(resolve => setTimeout(resolve, 1000));
      
      const { data: retryProfile } = await supabase
        .from('users')
        .select('id, email, role, status')
        .eq('id', user.id)
        .single();
      
      if (!retryProfile) {
        console.error('‚ùå Profile not found after trigger');
        return NextResponse.redirect(
          new URL('/auth/login?error=Profil+non+cr√©√©', requestUrl.origin)
        );
      }
    }
    
    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    // 6Ô∏è‚É£ REDIRECTION : Selon le r√¥le
    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    const finalProfile = profile || await supabase
      .from('users')
      .select('role')
      .eq('id', user.id)
      .single()
      .then(r => r.data);
    
    if (!finalProfile) {
      return NextResponse.redirect(new URL('/dashboard', requestUrl.origin));
    }
    
    // ADMIN, TREASURER, VALIDATOR ‚Üí Dashboard
    if (['ADMIN', 'TREASURER', 'VALIDATOR'].includes(finalProfile.role)) {
      return NextResponse.redirect(new URL('/dashboard', requestUrl.origin));
    }
    
    // MEMBER ‚Üí Claims (demandes)
    return NextResponse.redirect(new URL('/claims', requestUrl.origin));
    
  } catch (error: any) {
    console.error('‚ùå Callback exception:', error.message);
    return NextResponse.redirect(
      new URL('/auth/login?error=Erreur+serveur', requestUrl.origin)
    );
  }
}
